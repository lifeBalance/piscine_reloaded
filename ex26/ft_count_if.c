int	ft_count_if(char **tab, int (*f)(char*))
{
	int	c;
	int	i;

	c = 0;
	i = 0;
	while (tab[i] != 0)
	{
		if (f(tab[i]))
			c++;
		i++;
	}
	return (c);
}
